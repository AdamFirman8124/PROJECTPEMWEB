  function checkLogin(event) {
    event.preventDefault(); // Mencegah tautan mengikuti href

    // Cek apakah pengguna sudah login (misalnya menggunakan Laravel Blade directive)
    @if (Auth:: guest())
    Swal.fire({
      icon: 'warning',
      title: 'Maaf',
      text: 'Anda harus login terlebih dahulu untuk mendaftar seminar ini',
      showCancelButton: true,
      confirmButtonText: 'Login',
      cancelButtonText: 'Batal'
    }).then((result) => {
      if (result.isConfirmed) {
        window.location.href = "{{ route('login') }}"; // Arahkan ke halaman login
      }
    });
    @else
    // Jika pengguna sudah login, arahkan ke halaman pendaftaran seminar
    window.location.href = "{{ route('seminar.register', ['id' => $seminar->id]) }}";
    @endif
  }
